<?php
/* Com estes operadores podemos atribuir valor a uma variável;
O mais conhecido é o =, porém temos algumas variações do mesmo;
Operadores: +=, -=, /=, *= e %=;
Cada um destes fará uma operação antes da atribuição;
*/

$a = 0;

// +=
$a += 10; // $a = $a + 10
echo $a . "<br>";

// -=
$a -= 5; // $a = $a -5
echo $a . "<br>";

// *=
$a *= 2; // $a = $a * 2
echo $a . "<br>";


// /=
$a /= 2; // $a = $a / 2
echo $a . "<br>";


// %=
$a %= 2; // $a = $a % 2
echo $a . "<br>";
